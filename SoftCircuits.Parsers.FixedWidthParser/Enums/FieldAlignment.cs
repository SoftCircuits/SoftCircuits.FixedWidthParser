namespace SoftCircuits.Parsers
{
    /// <summary>
    /// Specifies fixed-width field alignment.
    /// </summary>
    public enum FieldAlignment
    {
        /// <summary>
        /// Field is aligned left by padding on the right.
        /// </summary>
        Left,

        /// <summary>
        /// Field is aligned right by padding on the left.
        /// </summary>
        Right,
    }
}
